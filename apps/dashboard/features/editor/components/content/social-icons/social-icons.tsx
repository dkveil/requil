export function SocialIcons({
	block,
	isCanvas,
	styles,
	interactionProps,
}: BlockRendererProps & {
	styles: React.CSSProperties;
	interactionProps: Record<string, unknown>;
}) {
	const mode = (block.props.mode as string) || 'horizontal';
	const iconSize = (block.props.iconSize as string) || '20px';
	const iconColor = (block.props.iconColor as string) || '#000000';
	const textMode = Boolean(block.props.textMode);
	const textPosition = (block.props.textPosition as string) || 'beside';
	const icons = (block.props.icons as SocialIcon[]) || [];

	// Get alignment from props - layoutGroup uses 'align' which has flex values
	const align = (block.props.align as string) || 'center';

	// Get typography props when text mode is enabled
	const fontSize = textMode ? (block.props.fontSize as number) || 13 : 13;
	const fontWeight = textMode
		? (block.props.fontWeight as string) || 'normal'
		: 'normal';
	const fontFamily = textMode ? (block.props.fontFamily as string) : undefined;
	const textColor = textMode
		? (block.props.textColor as string) || '#000000'
		: '#000000';

	// Convert flex alignment to justify-content
	const getJustifyContent = (alignment: string) => {
		switch (alignment) {
			case 'flex-start':
				return 'flex-start';
			case 'flex-end':
				return 'flex-end';
			case 'center':
				return 'center';
			case 'stretch':
				return 'center';
			case 'baseline':
				return 'center';
			default:
				return 'center';
		}
	};

	return (
		<div
			{...interactionProps}
			style={{
				...styles,
				display: 'flex',
				flexDirection: mode === 'vertical' ? 'column' : 'row',
				justifyContent: getJustifyContent(align),
				alignItems: 'center',
				gap: block.props.gap ? `${block.props.gap}px` : '4px',
			}}
			data-block-type='SocialIcons'
			data-block-id={block.id}
		>
			{icons.map((icon: SocialIcon) => {
				const networkLabel =
					icon.network.charAt(0).toUpperCase() + icon.network.slice(1);
				const iconSvg = NETWORK_ICONS[icon.network] || NETWORK_ICONS.web;

				return (
					<a
						key={icon.id}
						href={isCanvas ? undefined : icon.href}
						onClick={(e) => isCanvas && e.preventDefault()}
						title={icon.alt || networkLabel}
						style={{
							display: 'flex',
							flexDirection: textPosition === 'below' ? 'column' : 'row',
							alignItems: 'center',
							gap: textPosition === 'below' ? '2px' : '4px',
							textDecoration: 'none',
							color: '#000000',
							cursor: 'pointer',
						}}
					>
						<div
							style={{
								width: iconSize,
								height: iconSize,
								display: 'flex',
								alignItems: 'center',
								justifyContent: 'center',
								color: iconColor,
							}}
							dangerouslySetInnerHTML={{ __html: iconSvg as string }}
						/>
						{textMode && (
							<span
								style={{
									fontSize: `${fontSize}px`,
									fontWeight,
									fontFamily,
									color: textColor,
									lineHeight: 1,
								}}
							>
								{networkLabel}
							</span>
						)}
					</a>
				);
			})}
			{icons.length === 0 && (
				<div
					style={{
						color: '#9CA3AF',
						fontSize: '14px',
						padding: '16px 0',
					}}
				>
					Add social icons in the inspector panel
				</div>
			)}
		</div>
	);
}
